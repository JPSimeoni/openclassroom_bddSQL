CREATE DATABASE foodly ;
USE foodly ;
CREATE TABLE utilisateur (

id INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,

nom VARCHAR(100),

prenom VARCHAR(100),

email VARCHAR(255) NOT NULL UNIQUE

);
CREATE TABLE aliment (

id INTEGER NOT NULL AUTO_INCREMENT PRIMARY KEY,

nom VARCHAR(100) NOT NULL,

marque VARCHAR(100),

sucre FLOAT,

calories INTEGER NOT NULL,

graisses FLOAT,

proteines FLOAT,

BIO boolean

);